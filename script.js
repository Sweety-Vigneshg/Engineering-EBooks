// Function to navigate to department page
function navigateToDepartment(department) {
    if (department === 'CSE') {
        window.location.href = "CSE.html";
    } 
    if (department === 'ECE') {
        window.location.href = "ECE.html";
    }
    if (department === 'EEE') {
         window.location.href = "EEE.html";
    }
    if (department === 'MECH') {
        window.location.href = "MECH.html";
    }
    if (department === 'AIDS') {
        window.location.href = "AIDS.html";
    }
    if (department === 'AIML') {
        window.location.href = "AIML.html";
    }
    if (department === 'CIVIL') {
        window.location.href = "CIVIL.html";
    }
    else {
       // window.location.href = department + "_AIML.html";
    }
}

